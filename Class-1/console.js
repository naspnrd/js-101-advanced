// log() -> is most commonly used method for logging messages to the console

console.log("Hello, world!", "Neeraj");

// info() -> is similar to log () but used specifically for informational messages

console.info("This is javascript module classes");

// warn() -> used to log warning messages

console.warn("This is a warning message");

// error() -> used to log error messages

console.error("This is an error message");

console.clear();
